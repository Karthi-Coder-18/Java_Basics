package operators_in_java;

public class IncrementDecrementOperators {

    public static void main(String[] args){

        double h = 20.0D;
        System.out.println(++h); // To increase the current value by 1 use increment operator.

        float j = 22.0F;

        System.out.println(--j); // To decrease the current value by 1 use decrement operator


    }
}
